/**
 * Instantiate a new Connection class for a specific database driver.
 *
 * @param string $db Database Connection object to create. If undefined,
 *	use the last database driver that was loaded by ADOLoadCode().
	*
	* @return ADOConnection|false The freshly created instance of the Connection class
	*                             or false in case of error.
	*/
function ADONewConnection(?string $db=null,?string $customPath=null) {

	if (!$db) {
		die('must provide a valid ADOdb Driver');
	}
	$driverData = [
		'mysqli' => 'MySQL',
		'oci8'   => 'Oracle',
		'db2'    => 'IBMDB2',
		'sqlite3' => 'SQLite',
		'mssqlnative' => 'SqlServer',
		'pdo\mysql' => 'PDO\MySQL',
		'pdo\sqlite' => 'PDO\SQLite'
	];

	$from = ['/', '_'];
	$to   = ['\\', '\\'];
	$db = strtolower(str_replace($from, $to, $db));

	if (!array_key_exists($db, $driverData)) {
		die('Invalid Driver Name');
	}
	
	if ($customPath) {
		$classPath = $customPath;
		$driverPath = ltrim(str_replace('\\', '/',$classPath) . '.php','\\');
		$includePath = stream_resolve_include_path($driverPath);
	} else {
		$driverPath  = sprintf('ADOdb/Resources/%s/ADOConnection.php', $driverData[$db]);
		$includePath = stream_resolve_include_path($driverPath);
		$classPath   = sprintf('\ADOdb\Resources\%s\ADOConnection', $driverData[$db]);
	}

	require_once $includePath;
	
	$object = new $classPath($db);

	return $object;

}

function newADOConnection(?string $db=null,?string $customClass=null) {
	return ADONewConnection($db, $customClass);
}



/**
 * Get a new Data Dictionary object for the connection.
 *
 * @param ADOConnection $conn
 * @param string        $drivername
 *
 * @return ADODB_DataDict|false
 */
function newDataDictionary(&$conn, $drivername='') {
	
	$dataDictClassFile = sprintf(
		'%s/Resources/%s/DataDictionary.php',
		ADODB_DIR,
		$conn->getDataDictionaryProvider()
	);

	$dataDictClass = sprintf(
		'ADOdb\Resources\%s\DataDictionary',
		$conn->getDataDictionaryProvider()
	);

	require_once $dataDictClassFile;
	$conn->dictionaryObject = new $dataDictClass($conn);

	return $conn->dictionaryObject;

}

