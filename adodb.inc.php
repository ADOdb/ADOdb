    {
        if(empty($this->fields)) {
            return;
        }

        // Determine case conversion function
        $fn_change_case = $this->AssocCaseConvertFunction();
        if(!$fn_change_case) {
            // No conversion needed
            return;
        }

        $arr = array();

        // Change the case
        foreach($this->fields as $k => $v) {
            if (!is_integer($k)) {
                $k = $fn_change_case($k);
            }
            $arr[$k] = $v;
        }
        $this->fields = $arr;
    }

