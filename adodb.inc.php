    protected function AssocCaseConvertFunction($case = ADODB_ASSOC_CASE) 
    {
        switch($case) {
        case ADODB_ASSOC_CASE_UPPER:
            return 'strtoupper';
        case ADODB_ASSOC_CASE_LOWER:
