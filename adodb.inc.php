    {
        if (!$this->EOF) {
            $this->_currentRow++;

            $pos = $this->_currentRow;

            if ($this->_numOfRows <= $pos) {
                if (!$this->compat) {
                    $this->fields = false;
                }
            } else {
                if ($this->_skiprow1) {
                    $pos += 1;
                }
                $this->fields = $this->_array[$pos];
                return true;
            }
            $this->EOF = true;
        }

        return false;
    }

    /**
     * Sets the $fields member variable.
     * @return bool
     */
    function _fetch() 
    {
        $pos = $this->_currentRow;

        if ($this->_numOfRows <= $pos) {
            if (!$this->compat) {
                $this->fields = false;
            }
            return false;
        }
        if ($this->_skiprow1) {
            $pos += 1;
        }
        $this->fields = $this->_array[$pos];
        return true;
    }

    /**
     * Always returns true.
     * @return bool
     */
    function _close() 
    {
        return true;
    }

} // ADORecordSet_array

