     * (see ADODB_ASSOC_CASE_xxx constants)
     *
     * @return array
     */
    function GetRowAssoc($upper = ADODB_ASSOC_CASE) 
    {
        $record = array();
        $this->GetAssocKeys($upper);

        foreach($this->bind as $k => $v) {
            if(array_key_exists($v, $this->fields) ) {
